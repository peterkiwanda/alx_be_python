# Prompt the user to input their current age
current_age = int(input("How old are you? "))

# Define the current year and the future year
current_year = 2023
future_year = 2050

# Calculate how many years in the future
years_in_future = future_year - current_year

# Calculate the user's age in the future year
age_in_future = current_age + years_in_future

# Print the result
print(f"In {future_year}, you will be {age_in_future} years old.")

